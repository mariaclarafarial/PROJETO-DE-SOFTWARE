    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        MODELO.Provas inserirProvas = new MODELO.Provas();
        inserirProvas.setValor(textv.getText());
        inserirProvas.setConteudo(textc.getText());

        ProvasDAO lpDAO  = new ProvasDAO();
        if (lpDAO.Consultatual(String.valueOf(inserirProvas.getConteudo()))== null){
            if (lpDAO.inserirProvasDAO(inserirProvas)){
                JOptionPane.showMessageDialog(null, "Provas foram inseridas no sistema com sucesso.");
        }else{
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro ao tentar adicionar ficha.");            
            }
        }else{
            if(lpDAO.ATUALIZAR_PROVAS(inserirProvas)){
                JOptionPane.showMessageDialog(null, "Provas foram atualizadas no sistema com sucesso.");
        }else{
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro ao tentar atualizar datas.");            
            }
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

