    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        MODELO.Aluno inserirAluno = new MODELO.Aluno();
        inserirAluno.setNome(textn.getText());
        inserirAluno.setemail_Institucional(textei.getText());
        inserirAluno.setCurso(textc.getText());
        inserirAluno.setSerie(texts.getText());

        AlunoDAO laDAO  = new AlunoDAO();
        if (laDAO.Consultatual(String.valueOf(inserirAluno.getemail_Institucional()))== null){
            if (laDAO.inserirAlunoDAO(inserirAluno)){
                JOptionPane.showMessageDialog(null, "Aluno foi inserido no sistema com sucesso.");
        }else{
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro ao tentar adicionar ficha.");            
            }
        }else{
            if(laDAO.ATUALIZAR_ALUNO(inserirAluno)){
                JOptionPane.showMessageDialog(null, "Aluno foi atualizado no sistema com sucesso.");
        }else{
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro ao tentar atualizar aluno.");            
            }
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

